// Helper function to calculate player price based on rating
export const calculatePlayerPrice = (rating: number): number => {
  if (rating >= 99) return 1000; // 1000 coins for 99 OVR
  if (rating >= 90) return 500;  // 500 coins for 90-98 OVR
  if (rating >= 85) return 250;  // 250 coins for 85-89 OVR
  if (rating >= 80) return 100;  // 100 coins for 80-84 OVR
  return 50;                     // 50 coins for below 80 OVR
};

// Pack probability function - Updated with even lower chances for 99-rated players
export const getPackProbabilities = (packType: 'low' | 'medium' | 'high') => {
  switch (packType) {
    case 'high':
      return {
        99: 0.0001,  // 0.01% chance for 99 OVR
        90: 0.0999,  // 9.99% chance for 90+ OVR
        85: 0.3,     // 30% chance for 85-89 OVR
        80: 0.6      // 60% chance for 80-84 OVR
      };
    case 'medium':
      return {
        99: 0.0001,  // 0.01% chance for 99 OVR
        90: 0.0499,  // 4.99% chance for 90+ OVR
        85: 0.25,    // 25% chance for 85-89 OVR
        80: 0.7      // 70% chance for 80-84 OVR
      };
    case 'low':
    default:
      return {
        99: 0.0001,  // 0.01% chance for 99 OVR
        90: 0.0199,  // 1.99% chance for 90+ OVR
        85: 0.18,    // 18% chance for 85-89 OVR
        80: 0.8      // 80% chance for 80-84 OVR
      };
  }
};